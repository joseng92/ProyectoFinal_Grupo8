    private Usuario autenticarUsuario(String idUsuario, String contraseña) {
        for (Profesor profesor : baseDeDatos.getProfesores()) {
            if (profesor.getIdUsuario().equals(idUsuario) && profesor.getContraseña().equals(contraseña)) {
                return profesor;
            }
        }
        for (Estudiante estudiante : baseDeDatos.getEstudiantes()) {
            if (estudiante.getIdUsuario().equals(idUsuario) && estudiante.getContraseña().equals(contraseña)) {
                return estudiante;
            }
        }
        return null;
    }
