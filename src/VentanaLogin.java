    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        String idUsuario = txtId.getText();
        String contraseña = new String(txtPass.getPassword());
        Usuario usuario = autenticarUsuario(idUsuario, contraseña);

        if (usuario != null) {
            JOptionPane.showMessageDialog(this, "¡Bienvenido " + usuario.getNombre() + "!", "Inicio Exitoso", JOptionPane.INFORMATION_MESSAGE);
            VentanaPrincipal ventanaprincipal = new VentanaPrincipal(usuario, baseDeDatos);
            ventanaprincipal.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "ID de usuario o contraseña incorrectos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

